package Taquilla1;

public class Puesto{
	
	private int Numero;
	private String Letra;
	
	public Puesto(int numero, String letra) {
		this.Numero = numero;
		this.Letra = letra;
	}

	public int getNumero() {
		return Numero;
	}

	public String getLetra() {
		return Letra;
	}
	
	
}
